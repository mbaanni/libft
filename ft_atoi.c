#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	result;

	i = 0;
	sign = 1;
	result = 0;
	while (((str[i] >= 9 && str[i] <= 13) || (str[i] == ' ')) && str[i])
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]) && str[i])
	{
		result = (result * 10) + (str[i] - 48);
		i++;
	}
	return (result * sign);
}
