#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	if (s)
	{
		i = ft_strlen(s);
		i--;
		while (i >= 0)
		{
			f(i, s + i);
			i--;
		}
	}
}
